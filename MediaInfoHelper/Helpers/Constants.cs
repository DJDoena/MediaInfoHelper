namespace DoenaSoft.MediaInfoHelper.Helpers;

/// <summary>
/// Constants for special file extensions this library is aware of.
/// </summary>
public static class Constants
{
    /// <summary>
    /// DVD Profiler information file
    /// </summary>
    public const string DvdProfilerFileExtensionName = "dvdp";

    /// <summary>
    /// DVD Profiler information file with preceding dot.
    /// </summary>
    public const string DvdProfilerFileExtension = "." + DvdProfilerFileExtensionName;

    /// <summary>
    /// Youtube information file.
    /// </summary>
    public const string YoutubeFileExtensionName = "yt";

    /// <summary>
    /// DVD Profiler information file with preceding dot.
    /// </summary>
    public const string YoutubeFileExtension = "." + YoutubeFileExtensionName;

    /// <summary>
    /// Manual entry file.
    /// </summary>
    public const string ManualFileExtensionName = "man";

    /// <summary>
    /// Manual entry file with preceding dot.
    /// </summary>
    public const string ManualFileExtension = "." + ManualFileExtensionName;
}